package server_site;

public class TextTranslation
{
	 public String translateText(String text, String targetLanguage)
	    {
			 if(targetLanguage == "bm")
		        	return "Selamat pagi";
		        else if(targetLanguage == "ar")
		        	return " صباح الخير";
		        else if(targetLanguage == "ko")	
		        	return "좋은 아침";
		        else
		        	return "좋은 아침";
	    }	 
	    
}




